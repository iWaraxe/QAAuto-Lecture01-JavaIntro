package com.coherentsolutions.section02;

public class PrimitiveDataTypes {
    public static void main(String[] args) {
        // Example of different primitive data types
        boolean isLearning = true;
        byte smallestByte = Byte.MIN_VALUE;
        char letterA = 'a';
        short maxShort = Short.MAX_VALUE;
        int someInt = 100000;
        long someLong = 100000L;
        float someFloat = 234.5f;
        double someDouble = 123.4;

        // Print the variables to console
        System.out.println("\nPrimitive Data Types:");
        System.out.println("boolean: " + isLearning);
        System.out.println("byte: " + smallestByte);
        System.out.println("char: " + letterA);
        System.out.println("short: " + maxShort);
        System.out.println("int: " + someInt);
        System.out.println("long: " + someLong);
        System.out.println("float: " + someFloat);
        System.out.println("double: " + someDouble);

    }
}
