package com.coherentsolutions.section02.advanced.type_float;

public class FloatExample3 {
    public static void main(String[] args) {
        // Arithmetic operations on float variables
        final float f1 = 1.5f;
        final float f2 = 2.5f;
        final float sum = f1 + f2;
        final float difference = f1 - f2;
        final float product = f1 * f2;
        final float quotient = f2 / f1;
        final float remainder = f2 % f1;

        System.out.println("f1: " + f1);
        System.out.println("f2: " + f2);
        System.out.println("Sum: " + sum);
        System.out.println("Difference: " + difference);
        System.out.println("Product: " + product);
        System.out.println("Quotient: " + quotient);
        System.out.println("Remainder: " + remainder);

        // Working with literals
        final float f3 = 123.456f;
        System.out.println("Final float literal f3: " + f3);
    }
}
