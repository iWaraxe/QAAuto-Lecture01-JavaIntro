package com.coherentsolutions.section02.advanced.type_double;

public class DoubleExample3 {
    public static void main(String[] args) {
        // Arithmetic operations on double variables
        final double d1 = 1.5;
        final double d2 = 2.5;
        final double sum = d1 + d2;
        final double difference = d1 - d2;
        final double product = d1 * d2;
        final double quotient = d2 / d1;
        final double remainder = d2 % d1;

        System.out.println("d1: " + d1);
        System.out.println("d2: " + d2);
        System.out.println("Sum: " + sum);
        System.out.println("Difference: " + difference);
        System.out.println("Product: " + product);
        System.out.println("Quotient: " + quotient);
        System.out.println("Remainder: " + remainder);

        // Working with literals
        final double d3 = 123.456;
        System.out.println("Final double literal d3: " + d3);
    }
}
